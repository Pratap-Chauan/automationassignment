package automationassignment;

public class VariableSwap {
	
	public static void main(String[] args){

	    int k;
		int i = 10;

	    int j = 7;
	    
	    
	    
	    k=i;
	    i=j;
	    j=k;
	    
	    

	    System.out.println("value of i is " +i);

	   
	  
	    System.out.println("value of j is "+ j);

	      

	   }

}
